#include <stdio.h>

int main() {
    // Addition
    int a = 10;
    int b = 20;
    int sum = a + b;
    printf("Addition: %d + %d = %d\n", a, b, sum);

    // Subtraction
    int c = 30;
    int d = 15;
    int difference = c - d;
    printf("Subtraction: %d - %d = %d\n", c, d, difference);

    // Multiplication
    int e = 4;
    int f = 5;
    int product = e * f;
    printf("Multiplication: %d * %d = %d\n", e, f, product);

    // Division
    int g = 40;
    int h = 8;
    int quotient = g / h;
    printf("Division: %d / %d = %d\n", g, h, quotient);

    return 0;
}
